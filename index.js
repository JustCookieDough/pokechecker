function search() {
    pokemonIndex = POKEMON_NAME.indexOf(hash)
    if (pokemonIndex == -1) {
        notFound.className = "not-found"
        results.className = "results invis"
    }
    else {
        notFound.className = "not-found invis"
        results.className = "results"

        // basic info
        pkName.innerText = capitalize(POKEMON_NAME[pokemonIndex])
        pkNameAnchor.href = 'https://bulbapedia.bulbagarden.net/wiki/' + hash
        pkNumber.innerText = "#" + POKEMON_NUMBER[pokemonIndex].toString().padStart(3, "0")
        
        // typing
        var type1 = POKEMON_TYPE1[pokemonIndex]
        var type2 = POKEMON_TYPE2[pokemonIndex]
        var isDualType = type2 != -1

        removeChildren(pkTypingContainer)
        pkTypingContainer.appendChild(createTypeIcon(type1))
        if (isDualType) pkTypingContainer.appendChild(createTypeIcon(type2))

        // stats
        pkHp.innerHTML = POKEMON_HP[pokemonIndex]
        pkAtt.innerHTML = POKEMON_ATT[pokemonIndex]
        pkDef.innerHTML = POKEMON_DEF[pokemonIndex]
        pkSpa.innerHTML = POKEMON_SPA[pokemonIndex]
        pkSpd.innerHTML = POKEMON_SPD[pokemonIndex]
        pkSpe.innerHTML = POKEMON_SPE[pokemonIndex]
        pkTot.innerHTML = POKEMON_TOT[pokemonIndex]

        // effectiveness
        var effectivenessArray = Array.apply(null, Array(18)).map(() => { return 1 })

        for (let j = 0; j < effectivenessArray.length; j++) {
            effectivenessArray[j] = MATCHUPS[type1][j] * (isDualType ? MATCHUPS[type2][j] : 1)
        }

        updateEffectivenessByMultiplier(effectivenessArray, .25, pkVstrong);
        updateEffectivenessByMultiplier(effectivenessArray, .5, pkStrong);
        updateEffectivenessByMultiplier(effectivenessArray, 2, pkWeak);
        updateEffectivenessByMultiplier(effectivenessArray, 4, pkVweak);
        updateEffectivenessByMultiplier(effectivenessArray, 0, pkImmune);
    }
}

function createTypeIcon(index) {
    let type = TYPES[index]

    // create icon
    var icon = document.createElement("span")
    
    // add classes and inner text
    icon.className = `type ${type}`
    icon.innerText = capitalize(type)

    return icon
}

// capitalizes the words in a string
function capitalize(string) {
    var out = string.split(" ")
    out.forEach((word, i, arr) => arr[i] = word.charAt(0).toUpperCase() + word.slice(1))
    return out.join(" ")
}

function removeChildren(domNode) {
    while (domNode.firstChild) {
        domNode.removeChild(domNode.lastChild)
    }
}

function updateEffectivenessByMultiplier(effectivenessArr, mult, domNode) {
    removeChildren(domNode.lastChild)
    var added = false

    for (let i = 0; i < effectivenessArr.length; i++) {
        if (effectivenessArr[i] == mult) {
            domNode.lastChild.appendChild(createTypeIcon(i))
            added = true
        }
    }

    if (!added) { domNode.className = "p-ta-cat invis" } else { domNode.className = "p-ta-cat" }
}

